colors = {
    "Красный": (255, 0, 0),
    "Зелёный": (0, 255, 0),
    "Синий": (0, 0, 255),
    "Жёлтый": (255, 255, 0),
    "Фиолетовый": (128, 0, 128),
    "Белый": (255, 255, 255),
    "Чёрный": (0, 0, 0)
}
red = colors["Красный"]
blue = colors["Синий"]
mixed = ((red[0] + blue[0]) // 2, (red[1] + blue[1]) // 2, (red[2] + blue[2]) // 2)
print(f"Смешанный цвет (Красный + Синий): {mixed}")
purple = colors["Фиолетовый"]
inverted = (255 - purple[0], 255 - purple[1], 255 - purple[2])
print(f"Инвертированный Фиолетовый: {inverted}")