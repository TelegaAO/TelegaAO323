import sys
def create_list(n):
    """Создаёт список квадратов чисел от 0 до n-1."""
    return [x**2 for x in range(n)]
def create_gen(n):
    """Создаёт генератор квадратов чисел от 0 до n-1."""
    return (x**2 for x in range(n))
n = 1_000_000
lst = create_list(n)
gen = create_gen(n)
print(f"Размер списка: {sys.getsizeof(lst)} байт")
print(f"Размер генератора: {sys.getsizeof(gen)} байт")
# Анализ сложности по памяти:
# - У списка: O(n) — он сразу хранит все n элементов в памяти.
# - У генератора: O(1) — он не хранит элементы, а вычисляет их по одному при запросе.
#   Генератор занимает фиксированное количество памяти (независимо от n),
#   потому что хранит только текущее состояние (переменную x и счётчик).