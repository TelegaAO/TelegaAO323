import time

# Подготовка: размер списка и количество операций
LIST_SIZE = 100_000
OPS_COUNT = 1_000

# 1. Тест pop(0) — удаление из начала
lst1 = list(range(LIST_SIZE))
start_time = time.time()

for _ in range(OPS_COUNT):
    lst1.pop(0)  # Удаляем первый элемент

time_pop0 = time.time() - start_time

# 2. Тест pop() — удаление с конца
lst2 = list(range(LIST_SIZE))
start_time = time.time()

for _ in range(OPS_COUNT):
    lst2.pop()  # Удаляем последний элемент

time_pop = time.time() - start_time

# Вывод результатов
print(f"Время удаления {OPS_COUNT} элементов с начала (pop(0)): {time_pop0:.6f} секунд")
print(f"Время удаления {OPS_COUNT} элементов с конца (pop()):   {time_pop:.6f} секунд")

# Вывод и анализ сложности:
# Операция pop() имеет сложность O(1) — удаление с конца списка в Python
# реализовано мгновенно, так как не требует сдвига остальных элементов.
# Операция pop(0) имеет сложность O(n) — при удалении первого элемента
# все остальные n-1 элементов приходится сдвигать на одну позицию влево.
# Поэтому даже 1000 таких операций на большом списке работают очень медленно.
# Вывод: pop() во много раз быстрее pop(0). Для частого удаления с начала
# лучше использовать collections.deque, у которого pop слева — O(1).