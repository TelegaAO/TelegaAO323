# 1. Функция поиска
def task_1(arr):
    for i in arr:
        if i == 100:
            return True
    return False
# Сложность: O(n) — в худшем случае (если 100 нет или стоит в конце) 
# придётся пройти весь список из n элементов. Время растёт линейно с ростом длины списка.


# 2. Функция доступа
def task_2(arr):
    return arr[0] + arr[-1]
# Сложность: O(1) — доступ к элементу списка по индексу (первому или последнему) 
# выполняется за постоянное время, независимо от размера списка.


# 3. Функция пар
def task_3(arr):
    count = 0
    for i in arr:
        for j in arr:
            if i == j:
                count += 1
    return count
# Сложность: O(n^2) — два вложенных цикла, каждый проходит по всем n элементам. 
# Общее число операций: n * n = n², что даёт квадратичную сложность.